score = int(input("Бағаңызды жазыңыз: "))

if score >= 90:
    print("Өте жақсы! Сіздің бағаңыз  А")
else:
    if score >= 80:
      print("Жақсы! Сіздің бағаңыз - B")
    else:
      if score >= 70:
        print("Қанағаттандырарлық! Сіздің бағаңыз - C")
      else:
        if score >= 60:
          print("Сіздің бағаңыз - D. Тапсырманы қайталау керек.")
        else:
          print("Сіз экзаменнен құладыңыз")